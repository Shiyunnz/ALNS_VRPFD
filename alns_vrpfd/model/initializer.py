def _construct_depot_drone_tasks(
    instance: InstanceManager,
    original_truck_nodes: Sequence[Sequence[int]],
    truck_routes: Sequence[TruckRoute],
) -> List[DroneTask]:
    """Construct drone tasks that launch from depot and retrieve at truck routes."""
    """Construct drone tasks that launch from depot and retrieve at truck routes."""
    drone_spec = instance.vehicle_specs.get("drone")
    if drone_spec is None or drone_spec.number <= 0:
        return []
    if not truck_routes:
        return []

    demands = instance.customer_manager.demands()
    depot_start = instance.customer_manager.depot_start
    depot_end = instance.customer_manager.depot_end or depot_start
    node_index = {node: idx for idx,
                  node in enumerate(instance.all_node_ids())}
    truck_matrix = instance.distance_matrix("truck")
    drone_matrix = instance.distance_matrix("drone")
    drone_time_matrix = instance.time_matrix("drone")
    energy_model = DroneEnergyModel()
    battery_capacity = instance.robust_config.drone_battery_capacity or float(
        "inf")

    tasks: List[DroneTask] = []
    drone_ids = list(range(drone_spec.number)) or [0]
    available_depot_drones = list(drone_ids)
    next_depot_drone_idx = 0
    task_id = 1000  # High task_id to avoid conflict
    truck_cost_per_km = instance.vehicle_specs.get(
        "truck").unit_cost if instance.vehicle_specs.get("truck") else 1.0
    drone_cost_per_km = instance.vehicle_specs.get(
        "drone").unit_cost if instance.vehicle_specs.get("drone") else 1.0
    max_customers_per_task = 3

    candidates: List[Tuple[float, TruckRoute, int, List[int]]] = []

    for route_idx, route in enumerate(truck_routes):
        original_nodes = original_truck_nodes[route_idx]
        if len(original_nodes) <= 2:
            continue

        for start in range(1, len(original_nodes) - 1):
            prev_node = original_nodes[start - 1]
            customers: List[int] = []
            payload = 0.0

            for end in range(start, len(original_nodes) - 1):
                customer = original_nodes[end]
                if customer in (depot_start, depot_end):
                    break
                demand = demands.get(customer, 0.0)
                # if demand <= 0.0:
                #     break
                if payload + demand > drone_spec.capacity:
                    break

                customers.append(customer)
                payload += demand
                if len(customers) > max_customers_per_task:
                    break

                retrieve_node = original_nodes[end + 1]
                if retrieve_node not in route.nodes:
                    continue

                energy = _nominal_energy(
                    launch=depot_start,
                    customers=customers,
                    retrieve=retrieve_node,
                    demands=demands,
                    energy_model=energy_model,
                    time_matrix=drone_time_matrix,
                    node_index=node_index,
                )
                if energy > battery_capacity:
                    break

                truck_path = [prev_node, *customers, retrieve_node]
                truck_removed = _path_distance(
                    truck_matrix, truck_path, node_index)
                if truck_removed == float("inf"):
                    continue
                pre_idx = node_index.get(prev_node)
                ret_idx = node_index.get(retrieve_node)
                if pre_idx is None or ret_idx is None:
                    continue
                truck_added = truck_matrix[pre_idx][ret_idx]
                if truck_added == float("inf"):
                    continue

                drone_path = [depot_start, *customers, retrieve_node]
                drone_distance = _path_distance(
                    drone_matrix, drone_path, node_index)
                if drone_distance == float("inf"):
                    continue

                delta_cost = (
                    truck_cost_per_km * (truck_removed - truck_added)
                    - drone_cost_per_km * drone_distance
                )
                if delta_cost <= 0.0:
                    continue

                candidates.append((
                    delta_cost,
                    route,
                    retrieve_node,
                    list(customers),
                ))

    candidates.sort(key=lambda entry: entry[0], reverse=True)
    used_customers: Set[int] = set()
    used_retrievals: Set[Tuple[int, int]] = set()

    for gain, route, retrieve_node, customers in candidates:
        if next_depot_drone_idx >= len(available_depot_drones):
            break

        anchor = (route.id, retrieve_node)
        if anchor in used_retrievals:
            continue
        if any(customer in used_customers for customer in customers):
            continue

        depot_drone_id = available_depot_drones[next_depot_drone_idx]
        next_depot_drone_idx += 1
        payloads = _build_payloads(customers, demands)
        task = DroneTask(
            task_id=task_id,
            drone_id=depot_drone_id,
            launch_truck=None,
            launch_node=depot_start,
            customers=list(customers),
            land_truck=route.id,
            retrieve_node=retrieve_node,
            payloads=payloads,
        )
        tasks.append(task)
        task_id += 1
        used_retrievals.add(anchor)
        used_customers.update(customers)

    return tasks


def _path_distance(
    matrix: Sequence[Sequence[float]],
    nodes: Sequence[int],
    node_index: Mapping[int, int],
) -> float:
    distance = 0.0
    for start, end in zip(nodes, nodes[1:]):
        i = node_index.get(start)
        j = node_index.get(end)
        if i is None or j is None:
            return float("inf")
        seg = matrix[i][j]
        if seg == float("inf"):
            return float("inf")
        distance += seg
    return distance


