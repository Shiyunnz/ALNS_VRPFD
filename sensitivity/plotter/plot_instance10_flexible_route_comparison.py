def _to_float(value: Any) -> float:
    try:
        val = float(value)
    except (TypeError, ValueError):
        return math.nan
    return val


def _to_int(value: Any) -> int | None:
    if value is None or value == "":
        return None
    try:
        return int(value)
    except (TypeError, ValueError):
        return None


def _load_json_payload(text: str) -> List[Dict[str, Any]]:
    if not text:
        return []
    try:
        payload = json.loads(text)
        return payload if isinstance(payload, list) else []
    except json.JSONDecodeError:
        try:
            payload = ast.literal_eval(text)
            return payload if isinstance(payload, list) else []
        except (SyntaxError, ValueError):
            return []


def _instance_stem(path_text: str) -> str:
    return Path(path_text).stem if path_text else path_text


def infer_dataset_label(rows: List[Dict[str, Any]]) -> str:
    sizes: set[int] = set()
    for row in rows:
        stem = _instance_stem(str(row.get("instance", "")))
        match = re.match(r"R_\d+_(\d+)_\d+", stem)
        if not match:
            continue
        try:
            sizes.add(int(match.group(1)))
        except ValueError:
            continue
    if len(sizes) == 1:
        n = next(iter(sizes))
        return f"Instance{n} Same vs Flexible"
    if len(sizes) > 1:
        return "Mixed Instances Same vs Flexible"
    return "Same vs Flexible"


def load_best_rows(csv_path: Path) -> List[Dict[str, Any]]:
    rows: list[Dict[str, Any]] = []
    with csv_path.open("r", newline="", encoding="utf-8") as f:
        for row in csv.DictReader(f):
            rows.append(row)
    return rows


def parse_instance_coordinates(instance_path: Path) -> Dict[int, Tuple[float, float]]:
    coords: dict[int, tuple[float, float]] = {}
    in_customer_section = False
    with instance_path.open("r", encoding="utf-8") as f:
        for raw in f:
            line = raw.strip()
            if not line:
                continue
            if line == "CUSTOMER INFORMATION":
                in_customer_section = True
                continue
            if in_customer_section and line in {"Distance For Drone", "Distance For Truck", "VEHICLE INFORMATION"}:
                break
            if not in_customer_section:
                continue
            if line.lower().startswith("id"):
                continue
            parts = line.replace("\t", " ").split()
            if len(parts) < 3:
                continue
            try:
                node = int(parts[0])
                x = float(parts[1])
                y = float(parts[2])
            except ValueError:
                continue
            coords[node] = (x, y)
    return coords


def build_truck_edges(routes: Iterable[Dict[str, Any]]) -> set[Tuple[int, int]]:
    edges: set[tuple[int, int]] = set()
    for route in routes:
        nodes = route.get("nodes", [])
        if not isinstance(nodes, list):
            continue
        for i in range(len(nodes) - 1):
            try:
                u = int(nodes[i])
                v = int(nodes[i + 1])
            except (TypeError, ValueError):
                continue
            edges.add((u, v))
    return edges


def extract_drone_customers(tasks: Iterable[Dict[str, Any]]) -> set[int]:
    customers: set[int] = set()
    for task in tasks:
        payload = task.get("customers", [])
        if not isinstance(payload, list):
            continue
        for node in payload:
            try:
                customers.add(int(node))
            except (TypeError, ValueError):
                continue
    return customers


def count_cross_truck_tasks(tasks: Iterable[Dict[str, Any]]) -> int:
    total = 0
    for task in tasks:
        launch_truck = task.get("launch_truck")
        land_truck = task.get("land_truck")
        if launch_truck is None or land_truck is None:
            continue
        try:
            if int(launch_truck) != int(land_truck):
                total += 1
        except (TypeError, ValueError):
            continue
    return total


def analyze_row(row: Dict[str, Any]) -> RouteAnalysis:
    same_routes = _load_json_payload(row.get("same_truck_routes", ""))
    same_tasks = _load_json_payload(row.get("same_drone_tasks", ""))
    flex_routes = _load_json_payload(row.get("flexible_truck_routes", ""))
    flex_tasks = _load_json_payload(row.get("flexible_drone_tasks", ""))

    same_edges = build_truck_edges(same_routes)
    flex_edges = build_truck_edges(flex_routes)
    truck_edge_symdiff = len(same_edges.symmetric_difference(flex_edges))

    same_drone_customers = extract_drone_customers(same_tasks)
    flex_drone_customers = extract_drone_customers(flex_tasks)
    to_flex = len(flex_drone_customers - same_drone_customers)
    from_flex = len(same_drone_customers - flex_drone_customers)

    same_cross = count_cross_truck_tasks(same_tasks)
    flex_cross = count_cross_truck_tasks(flex_tasks)

    saving_pct = _to_float(row.get("flexible_saving_vs_same"))
    saving_component = max(0.0, saving_pct) if math.isfinite(saving_pct) else 0.0

    score = (
        saving_component * 10.0
        + max(0, flex_cross - same_cross) * 2.5
        + to_flex * 1.5
        + truck_edge_symdiff * 0.2
    )

    return RouteAnalysis(
        instance=row.get("instance", ""),
        same_seed=_to_int(row.get("same_seed")),
        flex_seed=_to_int(row.get("flex_seed")),
        same_cost=_to_float(row.get("same_cost")),
        flex_cost=_to_float(row.get("flexible_cost")),
        saving_pct=saving_pct,
        same_drone_tasks=len(same_tasks),
        flex_drone_tasks=len(flex_tasks),
        same_cross_truck_tasks=same_cross,
        flex_cross_truck_tasks=flex_cross,
        truck_edge_symdiff=truck_edge_symdiff,
        drone_customer_shift_to_flex=to_flex,
        drone_customer_shift_from_flex=from_flex,
        representative_score=score,
    )


def choose_representative(analyses: List[RouteAnalysis]) -> RouteAnalysis:
    """
    Pick a case that best highlights flexible mechanism:
    1) flexible introduces cross-truck retrieval
    2) truck-route change is as small as possible
    3) among ties, prefer larger cross-truck increase and better saving
    """
    cross_candidates = [
        item for item in analyses
        if item.flex_cross_truck_tasks > item.same_cross_truck_tasks
    ]
    if not cross_candidates:
        return max(analyses, key=lambda item: item.saving_pct)

    positive_cross = [item for item in cross_candidates if item.saving_pct >= 0]
    strong_saving = [item for item in positive_cross if item.saving_pct >= 2.0]
    if strong_saving:
        pool = strong_saving
    elif positive_cross:
        pool = positive_cross
    else:
        pool = cross_candidates

    return min(
        pool,
        key=lambda item: (
            item.truck_edge_symdiff,
            -(item.flex_cross_truck_tasks - item.same_cross_truck_tasks),
            -(item.saving_pct if math.isfinite(item.saving_pct) else -math.inf),
            item.drone_customer_shift_to_flex + item.drone_customer_shift_from_flex,
        )
    )


def choose_top_saving(analyses: List[RouteAnalysis]) -> RouteAnalysis:
    return max(analyses, key=lambda item: item.saving_pct)


