def _format_cross_tasks(tasks: List[Dict[str, Any]]) -> List[str]:
    lines: list[str] = []
    for task in tasks:
        launch_truck = task.get("launch_truck")
        land_truck = task.get("land_truck")
        if launch_truck is None or land_truck is None:
            continue
        if str(launch_truck) == str(land_truck):
            continue
        launch_node = task.get("launch_node")
        retrieve_node = task.get("retrieve_node")
        customers = task.get("customers", [])
        customer_text = ",".join(str(c) for c in customers) if isinstance(customers, list) else ""
        lines.append(f"T{launch_truck}@{launch_node} -> [{customer_text}] -> T{land_truck}@{retrieve_node}")
    return lines


def write_markdown_report(
    analyses: List[RouteAnalysis],
    target_row: Dict[str, Any],
    target_analysis: RouteAnalysis,
    out_path: Path,
    dataset_label: str,
) -> None:
    ranked = sorted(analyses, key=lambda item: item.saving_pct, reverse=True)
    flex_tasks = _load_json_payload(target_row.get("flexible_drone_tasks", ""))
    cross_lines = _format_cross_tasks(flex_tasks)

    lines: list[str] = []
    lines.append(f"# {dataset_label} Flexible Docking Route Analysis")
    lines.append("")
    lines.append("## Most Representative Case")
    lines.append(f"- Instance: `{_instance_stem(target_analysis.instance)}`")
    lines.append(f"- same seed: `{target_analysis.same_seed}`, flex seed: `{target_analysis.flex_seed}`")
    lines.append(f"- same cost: `{target_analysis.same_cost:.4f}`")
    lines.append(f"- flex cost: `{target_analysis.flex_cost:.4f}`")
    lines.append(f"- saving: `{target_analysis.saving_pct:.2f}%`")
    lines.append("- selection rule: `cross-truck appears` + `minimal truck-route change`")
    lines.append("")
    lines.append("## Route Change Signals")
    lines.append(f"- Truck edge symmetric difference: `{target_analysis.truck_edge_symdiff}`")
    lines.append(f"- Drone tasks: `{target_analysis.same_drone_tasks}` -> `{target_analysis.flex_drone_tasks}`")
    lines.append(
        f"- Cross-truck retrieval tasks: `{target_analysis.same_cross_truck_tasks}` -> "
        f"`{target_analysis.flex_cross_truck_tasks}`"
    )
    lines.append(f"- Customers shifted to drone in flexible: `{target_analysis.drone_customer_shift_to_flex}`")
    lines.append(f"- Customers shifted away from drone in flexible: `{target_analysis.drone_customer_shift_from_flex}`")
    lines.append("")
    lines.append("## Key Cross-Truck Tasks In Flexible")
    if cross_lines:
        for item in cross_lines:
            lines.append(f"- {item}")
    else:
        lines.append("- None")
    lines.append("")
    lines.append("## Top-3 Savings")
    for item in ranked[:3]:
        lines.append(
            f"- `{_instance_stem(item.instance)}`: {item.saving_pct:.2f}% "
            f"(same seed={item.same_seed}, flex seed={item.flex_seed})"
        )
    lines.append("")

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    args = parse_args()
    input_csv = Path(args.input_csv)
    output_dir = Path(args.output_dir)
    output_prefix = args.output_prefix

    if not input_csv.exists():
        raise FileNotFoundError(f"Input CSV not found: {input_csv}")

    rows = load_best_rows(input_csv)
    if not rows:
        raise ValueError("Input CSV is empty.")
    dataset_label = args.dataset_label.strip() or infer_dataset_label(rows)

    analyses = [analyze_row(row) for row in rows]
    top_saving_analysis = choose_top_saving(analyses)
    target_analysis = choose_representative(analyses)
    top_saving_row = next(row for row in rows if row.get("instance") == top_saving_analysis.instance)
    target_row = next(row for row in rows if row.get("instance") == target_analysis.instance)

    metrics_csv = output_dir / f"{output_prefix}_route_diff_metrics.csv"
    saving_svg = output_dir / f"{output_prefix}_saving_rank.svg"
    route_svg = output_dir / f"{output_prefix}_route_before_after.svg"
    dual_case_svg = output_dir / f"{output_prefix}_dual_case_comparison.svg"
    report_md = output_dir / f"{output_prefix}_route_analysis.md"

    write_metrics_csv(analyses, metrics_csv)
    plot_saving_ranking_svg(analyses, saving_svg, dataset_label)
    plot_route_before_after_svg(target_row, target_analysis, route_svg)
    plot_dual_case_comparison_svg(
        top_saving_row,
        top_saving_analysis,
        target_row,
        target_analysis,
        dual_case_svg,
        dataset_label,
    )
    write_markdown_report(analyses, target_row, target_analysis, report_md, dataset_label)

    print(f"Input: {input_csv}")
    print(f"Output dir: {output_dir}")
    print("Generated files:")
    print(f"  - {metrics_csv.name}")
    print(f"  - {saving_svg.name}")
    print(f"  - {route_svg.name}")
    print(f"  - {dual_case_svg.name}")
    print(f"  - {report_md.name}")
    print(f"Highest saving instance: {_instance_stem(top_saving_analysis.instance)}")
    print(f"Representative instance: {_instance_stem(target_analysis.instance)}")


if __name__ == "__main__":
    main()
