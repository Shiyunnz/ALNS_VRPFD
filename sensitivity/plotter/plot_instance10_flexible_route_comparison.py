def _svg_header(width: int, height: int) -> List[str]:
    return [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{height}" viewBox="0 0 {width} {height}">',
        "<style>",
        "text { font-family: Arial, sans-serif; fill: #111; }",
        ".title { font-size: 16px; font-weight: bold; }",
        ".label { font-size: 12px; }",
        ".tick { font-size: 10px; }",
        "</style>",
    ]


def _svg_footer() -> List[str]:
    return ["</svg>"]


def _line(x1: float, y1: float, x2: float, y2: float, color: str, width: float = 1.0, dash: bool = False) -> str:
    dash_attr = ' stroke-dasharray="4,3"' if dash else ""
    return (
        f'<line x1="{x1:.2f}" y1="{y1:.2f}" x2="{x2:.2f}" y2="{y2:.2f}" '
        f'stroke="{color}" stroke-width="{width}"{dash_attr}/>'
    )


def _polyline(points: List[Tuple[float, float]], color: str, width: float = 1.2, dash: bool = False) -> str:
    dash_attr = ' stroke-dasharray="5,4"' if dash else ""
    path = " ".join(f"{x:.2f},{y:.2f}" for x, y in points)
    return (
        f'<polyline fill="none" stroke="{color}" stroke-width="{width}"{dash_attr} '
        f'points="{path}"/>'
    )


def _arrow_head(
    x1: float,
    y1: float,
    x2: float,
    y2: float,
    color: str,
    size: float = 7.0,
    width: float = 5.0,
) -> str:
    dx = x2 - x1
    dy = y2 - y1
    length = math.hypot(dx, dy)
    if length < 1e-6:
        return ""

    ux = dx / length
    uy = dy / length
    base_x = x2 - ux * size
    base_y = y2 - uy * size
    px = -uy
    py = ux
    half_w = width / 2.0

    left_x = base_x + px * half_w
    left_y = base_y + py * half_w
    right_x = base_x - px * half_w
    right_y = base_y - py * half_w

    points = (
        f"{x2:.2f},{y2:.2f} "
        f"{left_x:.2f},{left_y:.2f} "
        f"{right_x:.2f},{right_y:.2f}"
    )
    return f'<polygon points="{points}" fill="{color}" stroke="{color}" stroke-width="0.6"/>'


def _draw_path_with_arrows(
    svg: List[str],
    points: List[Tuple[float, float]],
    color: str,
    width: float,
    *,
    dash: bool = False,
    arrow_size: float = 7.0,
    arrow_width: float = 5.0,
) -> None:
    if len(points) < 2:
        return
    for i in range(len(points) - 1):
        x1, y1 = points[i]
        x2, y2 = points[i + 1]
        svg.append(_line(x1, y1, x2, y2, color, width, dash=dash))
        arrow = _arrow_head(x1, y1, x2, y2, color, size=arrow_size, width=arrow_width)
        if arrow:
            svg.append(arrow)


def _circle(x: float, y: float, r: float, fill: str, stroke: str = "#000", width: float = 1.0) -> str:
    return (
        f'<circle cx="{x:.2f}" cy="{y:.2f}" r="{r:.2f}" fill="{fill}" '
        f'stroke="{stroke}" stroke-width="{width}"/>'
    )


def _rect(x: float, y: float, w: float, h: float, fill: str, stroke: str = "#000", width: float = 1.0) -> str:
    return (
        f'<rect x="{x:.2f}" y="{y:.2f}" width="{w:.2f}" height="{h:.2f}" fill="{fill}" '
        f'stroke="{stroke}" stroke-width="{width}"/>'
    )


def _text(x: float, y: float, value: str, cls: str = "label", anchor: str = "middle") -> str:
    return f'<text x="{x:.2f}" y="{y:.2f}" class="{cls}" text-anchor="{anchor}">{value}</text>'


def _transform_point(
    x: float,
    y: float,
    x_min: float,
    x_max: float,
    y_min: float,
    y_max: float,
    panel_x: float,
    panel_y: float,
    panel_w: float,
    panel_h: float,
    padding: float = 28.0,
) -> Tuple[float, float]:
    inner_x = panel_x + padding
    inner_y = panel_y + padding
    inner_w = panel_w - 2 * padding
    inner_h = panel_h - 2 * padding
    if math.isclose(x_max, x_min):
        tx = inner_x + inner_w / 2
    else:
        tx = inner_x + (x - x_min) / (x_max - x_min) * inner_w
    if math.isclose(y_max, y_min):
        ty = inner_y + inner_h / 2
    else:
        ty = inner_y + inner_h - (y - y_min) / (y_max - y_min) * inner_h
    return tx, ty


def plot_saving_ranking_svg(
    analyses: List[RouteAnalysis],
    out_path: Path,
    dataset_label: str,
) -> None:
    ranked = sorted(analyses, key=lambda item: item.saving_pct, reverse=True)
    if not ranked:
        return

    width = 1200
    height = 560
    left = 70
    right = 30
    top = 60
    bottom = 120
    inner_x = left
    inner_y = top
    inner_w = width - left - right
    inner_h = height - top - bottom

    values = [item.saving_pct for item in ranked]
    v_min = min(min(values), 0.0)
    v_max = max(max(values), 0.0)
    if math.isclose(v_min, v_max):
        v_min -= 1.0
        v_max += 1.0
    pad = (v_max - v_min) * 0.12
    v_min -= pad
    v_max += pad

    def to_y(v: float) -> float:
        return inner_y + (v_max - v) / (v_max - v_min) * inner_h

    svg: list[str] = []
    svg.extend(_svg_header(width, height))
    svg.append(_text(width / 2, 28, f"{dataset_label} Saving Ranking", "title"))
    svg.append(_line(inner_x, inner_y, inner_x, inner_y + inner_h, "#333", 1.2))
    svg.append(_line(inner_x, inner_y + inner_h, inner_x + inner_w, inner_y + inner_h, "#333", 1.2))
    zero_y = to_y(0.0)
    svg.append(_line(inner_x, zero_y, inner_x + inner_w, zero_y, "#777", 1.0))

    for tick in [v_min, 0.0, v_max]:
        y = to_y(tick)
        svg.append(_line(inner_x, y, inner_x + inner_w, y, "#d0d0d0", 0.7))
        svg.append(_text(inner_x - 8, y + 3, f"{tick:.1f}", "tick", "end"))

    n = len(ranked)
    group_w = inner_w / n
    bar_w = group_w * 0.64
    for i, item in enumerate(ranked):
        x_center = inner_x + (i + 0.5) * group_w
        y_val = to_y(item.saving_pct)
        y0 = to_y(0.0)
        bar_y = min(y_val, y0)
        bar_h = abs(y_val - y0)
        color = "#0A9396" if i == 0 else "#3A86FF"
        svg.append(_rect(x_center - bar_w / 2, bar_y, bar_w, bar_h, color, stroke="#1f2937", width=0.7))
        stem = _instance_stem(item.instance)
        svg.append(_text(x_center, inner_y + inner_h + 44, stem, "tick"))
        label_y = bar_y - 6 if item.saving_pct >= 0 else bar_y + bar_h + 12
        label = f"{item.saving_pct:.1f}% (S{item.same_seed}/F{item.flex_seed})"
        svg.append(_text(x_center, label_y, label, "tick"))

    avg = sum(values) / len(values)
    avg_y = to_y(avg)
    svg.append(_line(inner_x, avg_y, inner_x + inner_w, avg_y, "#E76F51", 1.6, dash=True))
    svg.append(_text(inner_x + 80, avg_y - 6, f"Avg: {avg:.2f}%", "tick", "start"))
    svg.append(_text(18, inner_y + inner_h / 2, "Saving %", "label"))

    svg.extend(_svg_footer())
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(svg), encoding="utf-8")


def _draw_solution_panel(
    svg: List[str],
    panel_x: float,
    panel_y: float,
    panel_w: float,
    panel_h: float,
    title: str,
    coords: Dict[int, Tuple[float, float]],
    truck_routes: List[Dict[str, Any]],
    drone_tasks: List[Dict[str, Any]],
) -> None:
    svg.append(_rect(panel_x, panel_y, panel_w, panel_h, "#fafafa", stroke="#d0d0d0", width=1.0))
    svg.append(_text(panel_x + panel_w / 2, panel_y + 20, title, "label"))

    if not coords:
        svg.append(_text(panel_x + panel_w / 2, panel_y + panel_h / 2, "No coordinates", "label"))
        return

    xs = [p[0] for p in coords.values()]
    ys = [p[1] for p in coords.values()]
    x_min, x_max = min(xs), max(xs)
    y_min, y_max = min(ys), max(ys)

    truck_palette = ["#3A86FF", "#FB8500", "#8338EC", "#06D6A0", "#EF476F"]
    truck_colors: dict[int, str] = {}
    for route in truck_routes:
        try:
            tid = int(route.get("truck_id", 0))
        except (TypeError, ValueError):
            tid = 0
        if tid not in truck_colors:
            truck_colors[tid] = truck_palette[len(truck_colors) % len(truck_palette)]

    # Truck routes
    for route in truck_routes:
        nodes = route.get("nodes", [])
        if not isinstance(nodes, list) or len(nodes) < 2:
            continue
        try:
            tid = int(route.get("truck_id", 0))
        except (TypeError, ValueError):
            tid = 0
        color = truck_colors.get(tid, "#3A86FF")
        points: list[tuple[float, float]] = []
        for node in nodes:
            if node not in coords:
                continue
            tx, ty = _transform_point(
                coords[node][0], coords[node][1],
                x_min, x_max, y_min, y_max,
                panel_x, panel_y, panel_w, panel_h
            )
            points.append((tx, ty))
        _draw_path_with_arrows(
            svg,
            points,
            color=color,
            width=2.2,
            dash=False,
            arrow_size=8.0,
            arrow_width=6.0,
        )

    # Drone tasks
    for task in drone_tasks:
        launch_node = task.get("launch_node")
        retrieve_node = task.get("retrieve_node")
        customers = task.get("customers", [])
        if not isinstance(customers, list):
            customers = []
        if launch_node is None or retrieve_node is None:
            continue
        try:
            path_nodes = [int(launch_node)] + [int(c) for c in customers] + [int(retrieve_node)]
        except (TypeError, ValueError):
            continue

        launch_truck = task.get("launch_truck")
        land_truck = task.get("land_truck")
        cross = (
            launch_truck is not None and land_truck is not None
            and str(launch_truck) != str(land_truck)
        )
        color = "#D62828" if cross else "#5E548E"
        points: list[tuple[float, float]] = []
        for node in path_nodes:
            if node not in coords:
                continue
            points.append(_transform_point(
                coords[node][0], coords[node][1],
                x_min, x_max, y_min, y_max,
                panel_x, panel_y, panel_w, panel_h
            ))
        _draw_path_with_arrows(
            svg,
            points,
            color=color,
            width=1.2 if not cross else 1.8,
            dash=True,
            arrow_size=7.0 if not cross else 8.0,
            arrow_width=4.5 if not cross else 5.5,
        )

    # Nodes
    sorted_nodes = sorted(coords.keys())
    depot_start = min(sorted_nodes)
    depot_end = max(sorted_nodes)
    for node in sorted_nodes:
        tx, ty = _transform_point(
            coords[node][0], coords[node][1],
            x_min, x_max, y_min, y_max,
            panel_x, panel_y, panel_w, panel_h
        )
        if node == depot_start:
            svg.append(_rect(tx - 5, ty - 5, 10, 10, "#2A9D8F"))
        elif node == depot_end and depot_end != depot_start:
            svg.append(_rect(tx - 5, ty - 5, 10, 10, "#F4A261"))
        else:
            svg.append(_circle(tx, ty, 4.0, "#F8F9FA", stroke="#222", width=1.0))
        svg.append(_text(tx + 6, ty - 4, str(node), "tick", "start"))


